"""The Claude Code plugin manifest — the one-install adoption bundle.

`claude-plugin/` packages the three runtime surfaces a Claude Code user installs in
one step — the DOS hooks (`hooks/hooks.json`), the DOS MCP server (`.mcp.json`), and
the generic skill pack (`skills/`) — with a repo-root marketplace catalog
(`.claude-plugin/marketplace.json`) pointing at it. This file pins the contract so
the bundle can't silently drift from the package it fronts:

  * **JSON validity + required fields** — every manifest parses and carries the keys
    the Claude Code plugin/marketplace schema requires (`name`; the marketplace's
    `owner`/`plugins[]`; each plugin's `name`/`source`).
  * **Version lockstep** — the plugin manifest's `version` tracks the package version
    (`dos.__version__`), the same single-source rule the release skill enforces for
    `pyproject.toml` ↔ `__init__.py`. A stale plugin version is the drift this
    catches.
  * **Hooks wire the REAL verbs** — the bundled hooks run the shipped `dos hook
    {pretool,posttool,stop}` verbs (via `python -m dos.cli`, robust against the
    `dos` console script not being on PATH in the host's Python), in the verified
    Claude Code settings shape. These are the same three moments `dos init --hooks`
    wires; the plugin is just the pre-packaged form.
  * **The MCP server is launchable as configured** — the `.mcp.json` command
    (`python -m dos_mcp.server`) actually imports and builds (the prerequisite the
    plugin assumes: `pip install 'dos-kernel[mcp]'`).
  * **Skills are in sync with source** — the bundled `claude-plugin/skills/` is a
    faithful copy of the single-sourced `src/dos/skills/` (+ the authored
    `dos-setup`), regenerated by `scripts/build_plugin.py`. This calls that
    script's own `--check` logic, so the test and the build agree by construction.
  * **The bundled generic skills still name no host** — the SKP litmus ("a shipped
    generic skill names no host"), re-run on the COPIES, so a copy that somehow
    diverged from the grep-clean source is caught here too.

The plugin lives OUTSIDE the kernel (it consumes `dos`; nothing under `src/dos/`
imports it) — the same one-way arrow as the release scripts and `.claude/` tooling.
"""

from __future__ import annotations

import json
import re
import subprocess
import sys
from pathlib import Path

import dos

# scripts/ is not importable as a package; load the build module by path so the
# test and the build share ONE definition of "in sync" (no second copy of the rule).
import importlib.util

_REPO_ROOT = Path(dos.__file__).resolve().parents[2]
_BUILD_PY = _REPO_ROOT / "scripts" / "build_plugin.py"

_spec = importlib.util.spec_from_file_location("_build_plugin", _BUILD_PY)
assert _spec and _spec.loader, f"cannot load {_BUILD_PY}"
build_plugin = importlib.util.module_from_spec(_spec)
_spec.loader.exec_module(build_plugin)


PLUGIN_DIR = _REPO_ROOT / "claude-plugin"
PLUGIN_MANIFEST = PLUGIN_DIR / ".claude-plugin" / "plugin.json"
PLUGIN_MCP = PLUGIN_DIR / ".mcp.json"
PLUGIN_HOOKS = PLUGIN_DIR / "hooks" / "hooks.json"
PLUGIN_SKILLS = PLUGIN_DIR / "skills"
MARKETPLACE = _REPO_ROOT / ".claude-plugin" / "marketplace.json"


def _load(path: Path) -> dict:
    return json.loads(path.read_text(encoding="utf-8"))


# ---------------------------------------------------------------------------
# Existence + JSON validity
# ---------------------------------------------------------------------------
def test_all_manifest_files_exist_and_parse():
    for p in (PLUGIN_MANIFEST, PLUGIN_MCP, PLUGIN_HOOKS, MARKETPLACE):
        assert p.is_file(), f"missing manifest: {p}"
        obj = _load(p)  # raises on invalid JSON
        assert isinstance(obj, dict), f"{p} is not a JSON object"


# ---------------------------------------------------------------------------
# plugin.json — required field + version lockstep with the package
# ---------------------------------------------------------------------------
def test_plugin_manifest_required_name():
    manifest = _load(PLUGIN_MANIFEST)
    assert manifest.get("name"), "plugin.json must declare a `name`"
    # kebab-case, the documented plugin-name shape.
    assert re.fullmatch(r"[a-z0-9]+(-[a-z0-9]+)*", manifest["name"]), \
        f"plugin name must be kebab-case: {manifest['name']!r}"


def test_plugin_version_tracks_package_version():
    """The plugin version is single-sourced from the package, like the release skill.

    A bundle fronting `dos-kernel X.Y.Z` should advertise that version; a drifted
    literal here is the staleness this pins. (The bump is part of `/release` — this
    only asserts they match, it doesn't choose the value.)
    """
    manifest = _load(PLUGIN_MANIFEST)
    assert manifest.get("version") == dos.__version__, (
        f"plugin.json version {manifest.get('version')!r} != package "
        f"{dos.__version__!r} — bump claude-plugin/.claude-plugin/plugin.json "
        f"(and the marketplace plugin entry) in lockstep with the release")


# ---------------------------------------------------------------------------
# marketplace.json — schema essentials + it points at the real plugin
# ---------------------------------------------------------------------------
def test_marketplace_required_shape():
    mk = _load(MARKETPLACE)
    assert mk.get("name"), "marketplace must declare a `name`"
    owner = mk.get("owner")
    assert isinstance(owner, dict) and owner.get("name"), \
        "marketplace `owner` must be an object with a `name`"
    plugins = mk.get("plugins")
    assert isinstance(plugins, list) and plugins, \
        "marketplace must list at least one plugin"
    for entry in plugins:
        assert entry.get("name"), "each plugin entry needs a `name`"
        assert entry.get("source"), "each plugin entry needs a `source`"


def test_marketplace_source_resolves_to_the_plugin():
    """The plugin `source` resolves (relative to the marketplace root) to a dir that
    holds a real `.claude-plugin/plugin.json` whose name matches the entry."""
    mk = _load(MARKETPLACE)
    market_root = MARKETPLACE.parent.parent  # the dir CONTAINING .claude-plugin/
    entry = next(e for e in mk["plugins"] if e["name"] == "dos-kernel")
    source = entry["source"]
    assert not source.startswith("../"), "source must not escape the marketplace root"
    resolved = (market_root / source).resolve()
    assert resolved == PLUGIN_DIR.resolve(), \
        f"source {source!r} resolves to {resolved}, expected {PLUGIN_DIR}"
    sub_manifest = _load(resolved / ".claude-plugin" / "plugin.json")
    assert sub_manifest["name"] == entry["name"], \
        "the plugin's own manifest name must match the marketplace entry name"


def test_marketplace_plugin_version_matches_plugin_manifest():
    mk = _load(MARKETPLACE)
    entry = next(e for e in mk["plugins"] if e["name"] == "dos-kernel")
    manifest = _load(PLUGIN_MANIFEST)
    # The entry MAY omit a version (then it inherits the plugin's own); if it
    # declares one, it must agree with the plugin manifest (no split-brain version).
    if "version" in entry:
        assert entry["version"] == manifest["version"], (
            f"marketplace entry version {entry['version']!r} != plugin manifest "
            f"{manifest['version']!r}")


# ---------------------------------------------------------------------------
# hooks.json — wires the REAL shipped verbs, in the CC settings shape
# ---------------------------------------------------------------------------
def _hook_commands(hooks_obj: dict, event: str) -> list[str]:
    cmds: list[str] = []
    for group in hooks_obj.get("hooks", {}).get(event, []):
        for h in group.get("hooks", []):
            c = h.get("command", "")
            if isinstance(c, str):
                cmds.append(c)
    return cmds


def test_hooks_wire_the_three_dos_verbs():
    hooks = _load(PLUGIN_HOOKS)
    # Each of the three DOS lifecycle moments is wired to its shipped verb. The
    # command runs via `python -m dos.cli` (not the bare `dos` script) so it works
    # even when the console script isn't on the host's PATH.
    expected = {
        "PreToolUse": "hook pretool",
        "PostToolUse": "hook posttool",
        "Stop": "hook stop",
    }
    for event, verb in expected.items():
        cmds = _hook_commands(hooks, event)
        assert cmds, f"no hook wired for {event}"
        assert any(("dos.cli " + verb) in c or ("dos " + verb) in c for c in cmds), \
            f"{event} does not run `{verb}`: {cmds}"
        # The entries carry the CC `type: command` shape.
        for group in hooks["hooks"][event]:
            for h in group["hooks"]:
                assert h.get("type") == "command", \
                    f"{event} hook entry must be type=command: {h}"


def test_hook_verbs_are_real_cli_subcommands():
    """Guard against a typo'd verb: each wired `hook <verb>` is a real `dos` command.

    Runs `python -m dos.cli hook <verb> --help` and asserts it isn't an
    argparse 'invalid choice' error. (The hooks themselves read stdin; --help is the
    safe way to confirm the subcommand EXISTS without feeding it an event.)
    """
    import os
    env = {**os.environ, "PYTHONPATH": str(_REPO_ROOT / "src")}
    for verb in ("pretool", "posttool", "stop"):
        proc = subprocess.run(
            [sys.executable, "-m", "dos.cli", "hook", verb, "--help"],
            capture_output=True, text=True, env=env,
        )
        assert proc.returncode == 0, (
            f"`dos hook {verb} --help` failed (rc={proc.returncode}): "
            f"{proc.stderr or proc.stdout}")

# ---------------------------------------------------------------------------
# .mcp.json — the server it names actually builds
# ---------------------------------------------------------------------------
def test_mcp_server_config_shape():
    mcp = _load(PLUGIN_MCP)
    servers = mcp.get("mcpServers")
    assert isinstance(servers, dict) and servers, ".mcp.json needs `mcpServers`"
    assert "dos" in servers, "the DOS server must be registered under `dos`"
    dos_server = servers["dos"]
    assert dos_server.get("command"), "the server entry needs a `command`"
    args = dos_server.get("args", [])
    # Launched as `python -m dos_mcp.server` — robust against the `dos-mcp` console
    # script not being on PATH in the host's interpreter.
    assert "dos_mcp.server" in args or "dos_mcp.server" in str(dos_server.get("command")), \
        f"server should launch dos_mcp.server: {dos_server}"


def test_mcp_server_actually_builds():
    """The prerequisite the plugin assumes: the MCP server imports + constructs.

    This is the same surface `tests/test_mcp_server.py` exercises; here it confirms
    the package the plugin's `.mcp.json` will launch is importable when the `[mcp]`
    extra is present, so the bundle isn't fronting a broken command.

    The `[mcp]` extra is optional (the kernel's own dependency set is PyYAML-only),
    so this SKIPS when `mcp` is absent — matching `tests/test_mcp_server.py`'s
    `importorskip`. Without the guard the test fails in a `[dev]`-only env on the
    server's own graceful install-hint `SystemExit`, which is correct behaviour, not
    a regression.
    """
    import pytest
    pytest.importorskip("mcp", reason="dos-mcp needs the optional `mcp` extra")
    from dos_mcp.server import build_server
    server = build_server()
    assert server is not None


