# ---------------------------------------------------------------------------
# bin/ launchers — REACHABLE, not merely present (present != reached)
# ---------------------------------------------------------------------------
def test_every_tracked_launcher_is_reachable_or_allowlisted():
    """A launcher shipped + tracked but invoked by no hook is dead code that LOOKS
    live. `test_hook_binaries_bundled.test_launchers_are_tracked` proves PRESENCE;
    this proves REACHABILITY — every tracked bin/ launcher is either referenced by
    a hooks.json command or on the manual-integration allowlist (the docs/204 §3
    presence-not-goal wall, turned inward on the plugin's own packaging).

    Regression target: dos-hook.ps1 shipped with a comment claiming hooks.json
    invoked it, but the bundle's hooks are `shell: bash` → the POSIX launcher, so
    the .ps1 was never reached. It is now allowlisted as a manual helper.
    """
    findings = build_plugin.check_launcher_reachability(_REPO_ROOT)
    assert not findings, (
        "a bundled launcher is tracked but no hook reaches it:\n  "
        + "\n  ".join(findings))


def test_reachability_check_actually_bites():
    """Guard the guard: prove the reachability check is not vacuous. With an empty
    allowlist, the unwired dos-hook.ps1 MUST be flagged — otherwise the check would
    silently pass on a future genuinely-orphaned launcher too."""
    import unittest.mock
    with unittest.mock.patch.object(build_plugin, "_MANUAL_LAUNCHERS", frozenset()):
        findings = build_plugin.check_launcher_reachability(_REPO_ROOT)
    assert any("dos-hook.ps1" in f for f in findings), (
        "with no allowlist the .ps1 should be flagged unreachable — the check is "
        f"blind if it is not. findings: {findings}")


def test_ps1_comment_does_not_claim_it_is_wired():
    """The .ps1 once falsely claimed 'hooks.json invokes this'. A manual-helper
    launcher must say so, not advertise itself as wired — the comment is the thing a
    future reader trusts."""
    ps1 = (PLUGIN_DIR / "bin" / "dos-hook.ps1").read_text(encoding="utf-8")
    assert "NOT wired by the bundled hooks.json" in ps1, \
        "dos-hook.ps1 must state it is not wired by the bundle (it's a manual helper)"
    assert "hooks.json invokes this" not in ps1, \
        "dos-hook.ps1 must not claim hooks.json invokes it (it does not)"


def test_bundled_skills_include_generic_pack_and_setup():
    found = {p.parent.name for p in PLUGIN_SKILLS.rglob("SKILL.md")}
    # Every generic-pack skill is bundled...
    src_skills = {d.name for d in (Path(dos.__file__).parent / "skills").iterdir()
                  if d.is_dir() and (d / "SKILL.md").is_file()}
    assert src_skills <= found, f"missing generic skills in plugin: {src_skills - found}"
    # ...plus the plugin-only skills the build authors: dos-setup (onboarding) and
    # dos-stats (surface the bundled dos-hook binary's observation log, docs/276).
    for plugin_skill in ("dos-setup", "dos-stats"):
        assert plugin_skill in found, f"the {plugin_skill} plugin skill must be bundled"


# The job literals the SKP design law forbids in a SHIPPED generic skill (the same
# set tests/test_skill_pack_generic.py greps for) — re-checked on the COPIES.
_FORBIDDEN_HOST_LITERALS = [
    r"docs/_plans",
    r"output/next-up",
    r"docs/dispatch:",
    r"\bapply\b",
    r"\btailor\b",
    r"\bdiscovery\b",
]


# The plugin-only skills the build AUTHORS (not generic-pack copies). They
# legitimately name the plugin's own surfaces — the `dos-kernel` pip package, the
# bundled `dos-hook` binary, `${CLAUDE_PLUGIN_ROOT}` — so the generic "names no host"
# litmus does not apply to them, the same way the driver layer is exempt from
# "kernel imports no host". Keep this in lockstep with build_plugin's `written` set.
_PLUGIN_ONLY_SKILLS = {"dos-setup", "dos-stats"}


def test_bundled_generic_skills_name_no_host():
    """The 'a shipped generic skill names no host' litmus, run on the bundled copies.

    The plugin-only authored skills (`_PLUGIN_ONLY_SKILLS`: dos-setup, dos-stats) are
    EXEMPT: they are not generic SKP screenplays — they name the plugin's own surfaces
    (the `dos-kernel` pip package, the bundled `dos-hook stats` verb) on purpose, so
    the no-host rule doesn't apply to them.
    """
    for skill_md in PLUGIN_SKILLS.rglob("SKILL.md"):
        if skill_md.parent.name in _PLUGIN_ONLY_SKILLS:
            continue
        text = skill_md.read_text(encoding="utf-8")
        for pat in _FORBIDDEN_HOST_LITERALS:
            assert not re.search(pat, text), (
                f"{skill_md.relative_to(_REPO_ROOT)} names a host literal "
                f"/{pat}/ — a shipped generic skill must name no host")
